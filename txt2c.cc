#include <iostream>
#include <fstream>
#include <string>
#include <cstring>
#include <cerrno>

using std::cerr;
using std::cout;
using std::ifstream;
using std::string;
using std::strerror;

int main(int argc, char **argv)
{
  if (argc <  3 || argc > 5)
    {
      cerr << "usage: " << argv[0]
           << " [--strip-trailing] [--no-static] <filename> <arrayname>\n";
      return 1;
    }

  bool do_strip_trailing = false;
  char const * static_decl = "static ";
  int i = 1;
  if (string(argv[i]) == "--strip-trailing")
    {
      do_strip_trailing = true;
      i++;
    }
  if (string(argv[i]) == "--no-static")
    {
      static_decl = "extern ";
      i++;
    }
  char const * fname = argv[i++];
  char const * arr = argv[i++];
  
  ifstream fin(fname);
  if (!fin)
    {
      int e = errno;
      cerr << "could not open " << fname << " for reading: "
           << strerror(e) << '\n';
      return 1;
    }

  char c;
  string dat;
  while(fin.get(c))
    dat += c;

  if (do_strip_trailing)
    {
      int last = dat.find_last_not_of(" \t\n");
      dat.erase(last + 1);
    }

  cout << "// DO NOT EDIT\n"
       << "// this file is automatically generated from " << fname << ",\n"
       << "// any changes you make will be destroyed when it is regenerated\n"
       << "\n\n"
       << static_decl
       << "char const " << arr << "_constant[" << (dat.size() + 1) << "] = {\n";

  for (unsigned int i = 0; i < dat.size(); ++i)
    {
      if (i == 0) cout << '\t';
      else if (i % 14 == 0) cout << "\n\t";
      cout << static_cast<int>(dat[i]) << ", ";
    }
  cout << "0\n};\n";
}

